/// Преобразует «Фамилия И.О.» или несколько через запятую в полные ФИО.
String expandTeacherName(String raw) {
  final trimmed = raw.trim();
  if (trimmed.isEmpty) return trimmed;

  final direct = teacherFullNames[trimmed];
  if (direct != null) return direct;

  if (trimmed.contains(',')) {
    return trimmed
        .split(',')
        .map((p) => expandTeacherName(p.trim()))
        .join(', ');
  }

  final parts = trimmed.split(RegExp(r'\s+'));
  if (parts.length >= 3 && parts[1].length > 2) {
    return trimmed;
  }

  final normalized = trimmed.replaceAll(RegExp(r'\s+'), ' ');
  return teacherFullNames[normalized] ?? trimmed;
}
